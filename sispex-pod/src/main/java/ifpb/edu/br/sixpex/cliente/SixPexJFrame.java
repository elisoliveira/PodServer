    private void inicializaEmailService() {
        try {
            emailServiceRemote = SixPexCliente.lookupEmailRemote();
        } catch (NotBoundException | RemoteException ex) {
            JOptionPane.showMessageDialog(null, ex.getMessage(), "SixPex", JOptionPane.ERROR_MESSAGE);
            System.exit(0);
        }
    }

    private void preencheJListComEmails() {
        try {
            List<String> emails = emailServiceRemote.getEmails();
            DefaultListModel listModel = new DefaultListModel();
            listModel.clear();
            for (int i = 0; i < emails.size(); i++) {
                listModel.add(i, emails.get(i));
            }
            jListEmails.setModel(listModel);
        } catch (RemoteException ex) {
            JOptionPane.showMessageDialog(null, ex.getMessage());
        }
    }

